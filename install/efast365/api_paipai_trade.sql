  `wuliuCode` varchar(50) DEFAULT '' COMMENT '物流单号',
  `wuliuDesc` varchar(50) DEFAULT '' COMMENT '物流备注',
  `dealPayFeeCommission` varchar(20) DEFAULT '' COMMENT '手续费字段，大于0的时候，表示卖家承担的手续费',
