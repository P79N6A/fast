  `dealType` varchar(20) DEFAULT '' COMMENT '订单类型 SELL_TYPE_ALL:所有类型SELL_TYPE_BIN:一口价 SELL_TYPE_AUCTION_SINGLE:单件拍卖 SELL_TYPE_B2C:b2c订单 SELL_TYPE_TSHOP:店铺街',
  `createTime` datetime DEFAULT NULL COMMENT '订单的创建时间',
  `dealEndTime` datetime DEFAULT NULL COMMENT '订单结束时间',
  `lastUpdateTime` datetime DEFAULT NULL COMMENT '订单的最后更新时间',
  `payTime` datetime DEFAULT NULL COMMENT '买家的付款时间',
